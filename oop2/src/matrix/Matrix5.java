public class Matrix5 {
	public static void main(String[] args) {
	
		int result = 0;
		for (int i = 1; i < 6; i++) {
			for (int j = 1; j < 6; j++) {
				result += 1;
				System.out.print(result+"\t");
			}
			System.out.println();
		}
	}
	

}
